const mumaker = require('mumaker');

module.exports = {
    name: 'metallic',
    alias: [],
    desc: 'Create a decorative 3D metal text effect',
    category: 'textmaker',
    usage: '.metallic <text>',
    reactions: {
        start: 'вҡҷпёҸ',
        success: 'рҹӘҷ'
    },

    execute: async (sock, m, { args, reply }) => {
        const text = args.join(' ');

        if (!text) {
            return reply(
                `в•ӯв”ҖвқҚ *METALLIC TEXT*\nв”Ӯ\nв”Ӯ вңҳ Provide text\nв”Ӯ\nв”Ӯ вҡү Usage: .metallic <text>\nв”Ӯ\nв”Ӯ р“„„ Example:\nв”Ӯ   .metallic Nick\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            );
        }

        try {
            const result = await mumaker.ephoto('https://en.ephoto360.com/impressive-decorative-3d-metal-text-effect-798.html', text);

            if (!result || !result.image) {
                throw new Error('No image URL received from the API');
            }

            await sock.sendMessage(m.chat, {
                image: { url: result.image },
      //          caption: `в•ӯв”ҖвқҚ *METALLIC TEXT*\nв”Ӯ\nв”Ӯ вң“ Generated!\nв”Ӯ\nв”Ӯ р“ғј Text: ${text}\nв”Ӯ\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            }, { quoted: m });

        } catch (err) {
            console.error('[METALLIC ERROR]', err.message);

            return reply(
                `в•ӯв”ҖвқҚ *METALLIC TEXT*\nв”Ӯ\nв”Ӯ вңҳ Failed to generate\nв”Ӯ\nв”Ӯ р“„Ү ${err.message}\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            );
        }
    }
};
