const mumaker = require('mumaker');

module.exports = {
    name: 'thunder',
    alias: [],
    desc: 'Create a thunder/lightning text effect',
    category: 'textmaker',
    usage: '.thunder <text>',
    reactions: {
        start: 'рҹҢ©пёҸ',
        success: 'вҡЎ'
    },

    execute: async (sock, m, { args, reply }) => {
        const text = args.join(' ');

        if (!text) {
            return reply(
                `в•ӯв”ҖвқҚ *THUNDER TEXT*\nв”Ӯ\nв”Ӯ вңҳ Provide text\nв”Ӯ\nв”Ӯ вҡү Usage: .thunder <text>\nв”Ӯ\nв”Ӯ р“„„ Example:\nв”Ӯ   .thunder Nick\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            );
        }

        try {
            const result = await mumaker.ephoto('https://en.ephoto360.com/thunder-text-effect-online-97.html', text);

            if (!result || !result.image) {
                throw new Error('No image URL received from the API');
            }

            await sock.sendMessage(m.chat, {
                image: { url: result.image },
           //     caption: `в•ӯв”ҖвқҚ *THUNDER TEXT*\nв”Ӯ\nв”Ӯ вң“ Generated!\nв”Ӯ\nв”Ӯ р“ғј Text: ${text}\nв”Ӯ\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            }, { quoted: m });

        } catch (err) {
            console.error('[THUNDER ERROR]', err.message);

            return reply(
                `в•ӯв”ҖвқҚ *THUNDER TEXT*\nв”Ӯ\nв”Ӯ вңҳ Failed to generate\nв”Ӯ\nв”Ӯ р“„Ү ${err.message}\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            );
        }
    }
};
