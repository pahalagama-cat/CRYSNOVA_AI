const mumaker = require('mumaker');

module.exports = {
    name: 'neont',
    alias: [],
    desc: 'Create a colorful neon light text effect',
    category: 'textmaker',
    usage: '.neont <text>',
    reactions: {
        start: 'рҹ’«',
        success: 'рҹҢҹ'
    },

    execute: async (sock, m, { args, reply }) => {
        const text = args.join(' ');

        if (!text) {
            return reply(
                `в•ӯв”ҖвқҚ *NEON TEXT*\nв”Ӯ\nв”Ӯ вңҳ Provide text\nв”Ӯ\nв”Ӯ вҡү Usage: .neont <text>\nв”Ӯ\nв”Ӯ р“„„ Example:\nв”Ӯ   .neont Nick\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            );
        }

        try {
            const result = await mumaker.ephoto('https://en.ephoto360.com/create-colorful-neon-light-text-effects-online-797.html', text);

            if (!result || !result.image) {
                throw new Error('No image URL received from the API');
            }

            await sock.sendMessage(m.chat, {
                image: { url: result.image },
              //  caption: `в•ӯв”ҖвқҚ *NEON TEXT*\nв”Ӯ\nв”Ӯ вң“ Generated!\nв”Ӯ\nв”Ӯ р“ғј Text: ${text}\nв”Ӯ\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            }, { quoted: m });

        } catch (err) {
            console.error('[NEONT ERROR]', err.message);

            return reply(
                `в•ӯв”ҖвқҚ *NEON TEXT*\nв”Ӯ\nв”Ӯ вңҳ Failed to generate\nв”Ӯ\nв”Ӯ р“„Ү ${err.message}\nв•°в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ`
            );
        }
    }
};
